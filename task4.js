// Завдання 4.1 - Площа кола
let radius = 5; // Радіус кола
let areaCircle = Math.PI * Math.pow(radius, 2); // Площа кола за формулою: π * r^2
console.log("Площа кола:", areaCircle.toFixed(2)); // Округлення до 2 знаків після крапки

// Завдання 4.2 - Площа прямокутника
let length = 10; // Довжина прямокутника
let width = 4;   // Ширина прямокутника
let areaRectangle = length * width; // Площа прямокутника за формулою: довжина * ширина
console.log("Площа прямокутника:", areaRectangle.toFixed(2)); // Округлення до 2 знаків після крапки

// Завдання 4.3 - Об'єм циліндра
let cylinderRadius = 3; // Радіус циліндра
let height = 7;         // Висота циліндра
let volumeCylinder = Math.PI * Math.pow(cylinderRadius, 2) * height; // Об'єм циліндра за формулою: π * r^2 * h
console.log("Об'єм циліндра:", volumeCylinder.toFixed(2)); // Округлення до 2 знаків після крапки
